DROP TABLE DEPARTMENT CASCADE CONSTRAINTS;
DROP TABLE PROFESSOR CASCADE CONSTRAINTS;
DROP TABLE STUDENT CASCADE CONSTRAINTS;

CREATE TABLE DEPARTMENT (
	D_CODE VARCHAR2(4) PRIMARY KEY,
	D_NAME VARCHAR2(20),
	FACULTY VARCHAR2(10),
	COLLEGE VARCHAR2(10)
);

CREATE TABLE PROFESSOR (
	P_CODE VARCHAR2(4) PRIMARY KEY,
	P_NAME VARCHAR2(10),
	P_PHONE_NO VARCHAR2(30),
	ROOM_NO VARCHAR2(5),
	D_CODE VARCHAR2(4) REFERENCES DEPARTMENT(D_CODE)
);

CREATE TABLE STUDENT (
	STU_NO VARCHAR2(10) PRIMARY KEY,
	STU_NAME VARCHAR2(10),
	PASSWORD VARCHAR2(8),
	STU_PHONE_NO VARCHAR2(30),
	YEAR VARCHAR2(4),
	P_CODE VARCHAR2(4) REFERENCES PROFESSOR(P_CODE),
	D_CODE VARCHAR2(4) REFERENCES DEPARTMENT(D_CODE) 
);

